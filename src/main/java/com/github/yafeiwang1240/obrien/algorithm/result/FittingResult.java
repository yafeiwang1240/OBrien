package com.github.yafeiwang1240.obrien.algorithm.result;

public class FittingResult {
    private double a;
    private double b;

    public FittingResult(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
